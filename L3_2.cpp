#include <iostream>
using namespace std;

int main()  {
int a;
cout<<"size of integer "<<sizeof(a)<<endl;

float b;
cout<<"size of float "<<sizeof(b)<<"\n";

char d;
cout<<"size of character "<<sizeof(d)<<endl;

bool e;
cout<<"size of boolean "<<sizeof(e)<<endl;

short int f;
cout<<"size of short integer "<<sizeof(f)<<endl;

long int g;
cout<<"size of long integer "<<sizeof(g)<<"\n";

signed int h;
cout<<"size of signed integer "<<sizeof(h)<<"\n";

unsigned int i;
cout<<"size of unsigned integer "<<sizeof(i);

 return 0;
}